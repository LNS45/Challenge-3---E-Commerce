export function animaciones(){
    const redes = document.querySelector(".contacto__contenedor");
    redes.setAttribute('data-aos',"fade-up");
    redes.setAttribute('data-aos-delay',"100");
    redes.setAttribute('data-aos-once',"true");
    redes.setAttribute('data-aos-duration',"2000");

}
